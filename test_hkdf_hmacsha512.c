int main(int argc, char ** argv)
{
    int ret = 0; /* Return value is the number of failed test cases */
    unsigned char prk[MAX_TEST_ARRAY_LEN];
    unsigned char okm[MAX_TEST_ARRAY_LEN];
    int i;

    for (i = 0; i < sizeof (test) / sizeof (test[0]); ++i) {
        int OK = 1; /* TRUE */ 
        memset(prk, 0, sizeof(prk) / sizeof(prk[0]));
        memset(okm, 0, sizeof(okm) / sizeof(okm[0]));

        OK = OK && (0 == hkdf_hmacsha512_extract(
                                    test[i].salt,
                                    test[i].salt_len,
                                    test[i].ikm,
                                    test[i].ikm_len,
                                    prk,
                                    test[i].prk_len));

        OK = OK && (0 == memcmp(prk, test[i].prk, test[i].prk_len));

        OK = OK && (0 == hkdf_hmacsha512_expand(
                                    prk,
                                    test[i].prk_len,
                                    test[i].info,
                                    test[i].info_len,
                                    okm,
                                    test[i].okm_len));
        OK = OK && (0 == memcmp(okm, test[i].okm, test[i].okm_len));
        if (!OK) {
            printf("Fail: test %d\n", i);
            ++ret;
        } 
        else {
            printf("Pass: test %d\n", i);
        }
    } /* end for */

    return ret;
}
